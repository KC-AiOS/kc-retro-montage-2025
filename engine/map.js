//------------------------------------------------------
// 判斷某位置是否可走
//------------------------------------------------------

function canWalk(x, y) {
    // 超界 → 視為牆
    if (x < 0 || y < 0 || x >= MAP_W || y >= MAP_H) return false;

    return map[y][x] === 0;
}
